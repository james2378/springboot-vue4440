<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				chepiaobianhao : false,
				chezhanmingcheng : false,
				cheliangmingcheng : false,
				zuoweileixing : false,
				chufadi : false,
				mudedi : false,
				chufashijian : false,
				banliejieshao : false,
				tupian : false,
				chepiaojiage : false,
				thumbsupnum : false,
				crazilynum : false,
				discussnum : false,
				storeupnum : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          chepiaobianhao: this.getUUID(),
          chezhanmingcheng: '',
          cheliangmingcheng: '',
          zuoweileixing: '',
          chufadi: '',
          mudedi: '',
          chufashijian: '',
          banliejieshao: '',
          tupian: '',
          chepiaojiage: '',
          thumbsupnum: '',
          crazilynum: '',
          discussnum: '',
          storeupnum: '',
        },
        zuoweileixingOptions: [],


        rules: {
          chepiaobianhao: [
          ],
          chezhanmingcheng: [
          ],
          cheliangmingcheng: [
            { required: true, message: '车辆名称不能为空', trigger: 'blur' },
          ],
          zuoweileixing: [
            { required: true, message: '座位类型不能为空', trigger: 'blur' },
          ],
          chufadi: [
            { required: true, message: '出发地不能为空', trigger: 'blur' },
          ],
          mudedi: [
            { required: true, message: '目的地不能为空', trigger: 'blur' },
          ],
          chufashijian: [
            { required: true, message: '出发时间不能为空', trigger: 'blur' },
          ],
          banliejieshao: [
          ],
          tupian: [
            { required: true, message: '图片不能为空', trigger: 'blur' },
          ],
          chepiaojiage: [
            { required: true, message: '车票价格不能为空', trigger: 'blur' },
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          thumbsupnum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          crazilynum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          discussnum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          storeupnum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='chepiaobianhao'){
              this.ruleForm.chepiaobianhao = obj[o];
              this.ro.chepiaobianhao = true;
              continue;
            }
            if(o=='chezhanmingcheng'){
              this.ruleForm.chezhanmingcheng = obj[o];
              this.ro.chezhanmingcheng = true;
              continue;
            }
            if(o=='cheliangmingcheng'){
              this.ruleForm.cheliangmingcheng = obj[o];
              this.ro.cheliangmingcheng = true;
              continue;
            }
            if(o=='zuoweileixing'){
              this.ruleForm.zuoweileixing = obj[o];
              this.ro.zuoweileixing = true;
              continue;
            }
            if(o=='chufadi'){
              this.ruleForm.chufadi = obj[o];
              this.ro.chufadi = true;
              continue;
            }
            if(o=='mudedi'){
              this.ruleForm.mudedi = obj[o];
              this.ro.mudedi = true;
              continue;
            }
            if(o=='chufashijian'){
              this.ruleForm.chufashijian = obj[o];
              this.ro.chufashijian = true;
              continue;
            }
            if(o=='banliejieshao'){
              this.ruleForm.banliejieshao = obj[o];
              this.ro.banliejieshao = true;
              continue;
            }
            if(o=='tupian'){
              this.ruleForm.tupian = obj[o].split(",")[0];
              this.ro.tupian = true;
              continue;
            }
            if(o=='chepiaojiage'){
              this.ruleForm.chepiaojiage = obj[o];
              this.ro.chepiaojiage = true;
              continue;
            }
            if(o=='thumbsupnum'){
              this.ruleForm.thumbsupnum = obj[o];
              this.ro.thumbsupnum = true;
              continue;
            }
            if(o=='crazilynum'){
              this.ruleForm.crazilynum = obj[o];
              this.ro.crazilynum = true;
              continue;
            }
            if(o=='discussnum'){
              this.ruleForm.discussnum = obj[o];
              this.ro.discussnum = true;
              continue;
            }
            if(o=='storeupnum'){
              this.ruleForm.storeupnum = obj[o];
              this.ro.storeupnum = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.zuoweileixingOptions = "商务座,一等座,一等座,其它座位".split(',')

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`chepiaoxinxi/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			if(this.ruleForm.chepiaobianhao){
				this.ruleForm.chepiaobianhao = String(this.ruleForm.chepiaobianhao)
			}
			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('chepiaoxinxi/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`chepiaoxinxi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`chepiaoxinxi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      tupianUploadChange(fileUrls) {
          this.ruleForm.tupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

